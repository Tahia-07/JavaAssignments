package Operator;

public class ArithmeticOperator {
    public static void main(String[] args) {
        double number1= 80.22, number2=10.50;

        System.out.println(number1*number2);//842.31
        System.out.println(number1/number2);//7.64
        System.out.println(number2/number1);//0.13089...
        System.out.println(number1%number2);//6.71...
        System.out.println(number1+number2);
        System.out.println(number1-number2);
    }
}
